
package model;

public class StudentPGP {
	int studentId;
	String studentName;
	String emailId;
	Marks regular;
	Marks supply;
	
	public int getStudentId() {
		return studentId;
	}
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public Marks getRegular() {
		return regular;
	}
	public void setRegular(Marks regular) {
		this.regular = regular;
	}
	public Marks getSupply() {
		return supply;
	}
	public void setSupply(Marks supply) {
		this.supply = supply;
	}
		
}

	


